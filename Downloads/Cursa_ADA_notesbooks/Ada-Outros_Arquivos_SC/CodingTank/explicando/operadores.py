# Operador de negação (not)
valor_verdadeiro = True
valor_falso = not valor_verdadeiro
print(valor_falso)  # Saída: False

# Operador E (and)
valor1 = True
valor2 = False
resultado = valor1 and valor2
print(resultado)  # Saída: False

# Operador OU (or)
valor1 = True
valor2 = False
resultado = valor1 or valor2
print(resultado)  # Saída: True

# Operador OU exclusivo (xor)
valor1 = True
valor2 = False
resultado = valor1 ^ valor2
print(resultado)  # Saída: True

# Operador de igualdade (==)
valor1 = 10
valor2 = 20
resultado = valor1 == valor2
print(resultado)  # Saída: False

# Operador de desigualdade (!=)
valor1 = 10
valor2 = 20
resultado = valor1 != valor2
print(resultado)  # Saída: True

# Operador de maior que (>)
valor1 = 10
valor2 = 20
resultado = valor1 > valor2
print(resultado)  # Saída: False

# Operador de menor que (<)
valor1 = 10
valor2 = 20
resultado = valor1 < valor2
print(resultado)  # Saída: True

# Operador de maior ou igual que (>=)
valor1 = 10
valor2 = 20
resultado = valor1 >= valor2
print(resultado)  # Saída: False

# Operador de menor ou igual que (<=)
valor1 = 10
valor2 = 20
resultado = valor1 <= valor2
print(resultado)  # Saída: True
